int main() {
  char line[100];
  while (printf("ashish>"), gets(line) != NULL) {
    if (fork() == 0) {
      if ((execlp(line, line, (char *)0)) == -1) {
        perror("execlp failed");
        printf("%s: not found\n", line);
	}
    } else  wait(0);
  }
  return EXIT_SUCCESS;
}
